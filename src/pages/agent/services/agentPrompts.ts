/**
 * 获取 Agent 系统 Prompt
 */
export function getAgentSystemPrompt(agentType: AgentType, selectedBrand?: string, selectedStyle?: string, excludedElements?: string[], projectType?: string): string {
  const brandContext = selectedBrand
    ? `\n\n## 品牌背景\n用户正在为品牌"${selectedBrand}"进行设计。请在回复中考虑该品牌的文化背景、历史故事和品牌调性。`
    : '';

  const styleContext = selectedStyle
    ? `\n\n## 风格要求\n用户选择了"${selectedStyle}"风格。请在回复和设计中体现该风格的特点和美学特征。`
    : '';

  const exclusionContext = excludedElements && excludedElements.length > 0
    ? `\n\n## 用户明确排除的元素\n用户特别说明**不要**包含以下元素：${excludedElements.join('、')}。请尊重用户的意愿，在设计中避免使用这些元素。`
    : '';

  // 根据设计类型添加个性化上下文
  let projectTypeContext = '';
  if (projectType) {
    const projectTypeMap: Record<string, string> = {
      'packaging': '包装设计：产品包装、礼盒设计，需要考虑材质、结构、视觉呈现',
      'poster': '海报设计：宣传物料、推广海报，需要考虑排版、视觉冲击力',
      'brand-design': '品牌设计：Logo、VI系统、品牌视觉识别',
      'ip-character': 'IP形象设计：角色、吉祥物、虚拟形象',
      'illustration': '插画设计：手绘风格、艺术创作',
      'animation': '动画视频：动态视觉、短视频制作'
    };
    const projectTypeDesc = projectTypeMap[projectType] || projectType;
    projectTypeContext = `\n\n## 当前设计类型\n用户选择的是"${projectTypeDesc}"。请在回复中体现对该设计类型的专业理解，并提供针对性的建议和问题。`;
  }

  switch (agentType) {
    case 'director':
      return DIRECTOR_SYSTEM_PROMPT + brandContext + styleContext + exclusionContext + projectTypeContext;
    case 'designer':
      return DESIGNER_SYSTEM_PROMPT + brandContext + styleContext + exclusionContext + projectTypeContext;
    case 'illustrator':
      return ILLUSTRATOR_SYSTEM_PROMPT + brandContext + styleContext + exclusionContext + projectTypeContext;
    case 'copywriter':
      return COPYWRITER_SYSTEM_PROMPT + brandContext + styleContext + exclusionContext + projectTypeContext;
    case 'animator':
      return ANIMATOR_SYSTEM_PROMPT + brandContext + styleContext + exclusionContext + projectTypeContext;
    case 'researcher':
      return RESEARCHER_SYSTEM_PROMPT + brandContext + styleContext + exclusionContext + projectTypeContext;
    default:
      return DIRECTOR_SYSTEM_PROMPT + brandContext + styleContext;
  }
}

/**
 * 构建委派 Prompt
 */
export function buildDelegationPrompt(
  fromAgent: AgentType,
  toAgent: AgentType,
  taskDescription: string,
  reasoning: string,
  contextMessages: AgentMessage[]
): string {
  const fromConfig = AGENT_CONFIG[fromAgent];
  const toConfig = AGENT_CONFIG[toAgent];

  const contextSummary = contextMessages
    .slice(-3)
    .map(m => `${m.role === 'user' ? '用户' : fromConfig.name}: ${m.content.substring(0, 150)}`)
    .join('\n');

  return `${toConfig.name}你好！

我是${fromConfig.name}，现在将一项任务委派给你。

## 委派理由
${reasoning}

## 任务描述
${taskDescription}

## 对话上下文
${contextSummary}

## 你的任务
请基于以上信息，以你的专业能力为用户提供服务。直接回复用户，不需要提及这次委派。`;
}

